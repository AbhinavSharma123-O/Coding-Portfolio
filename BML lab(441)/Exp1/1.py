# Program to demonstrate usage of various mathematical functions using f-strings

import math

# Input values
a = 10.7
b = 25
c = 36
x = 20
y = 28

print(f"Floor of {a} = {math.floor(a)}")
print(f"Ceil of {a} = {math.ceil(a)}")
print(f"Square root of {c} = {math.sqrt(c)}")
print(f"Integer square root of {b} = {math.isqrt(b)}")
print(f"GCD of {x} and {y} = {math.gcd(x, y)}")
print(f"Absolute value of -15 = {math.fabs(-15)}")
print(f"Factorial of 5 = {math.factorial(5)}")
print(f"2 raised to power 3 = {math.pow(2, 3)}")
